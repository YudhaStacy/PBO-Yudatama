    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        String nim = txtNim.getText();
        String nama = txtNama.getText();
        String jk = ""; // Bisa diambil dari radio button
        String telepon = txtTlp.getText();
        String alamat = txtAlamat.getText();
        String prodi = txtProdi.getText();

        if (rbPria.isSelected()) {
            jk = "Laki-laki";
        } else if (rbWanita.isSelected()) {
            jk = "Perempuan";
        }

        // Query SQL untuk mengubah data
        String sql = "UPDATE mahasiswa SET nama = ?, jk = ?, telepon = ?, alamat = ?, prodi = ? WHERE nim = ?";

        try {
            // Siapkan koneksi dan prepared statement
            PreparedStatement stmt = conn.prepareStatement(sql);

            // Set parameter untuk query
            stmt.setString(1, nama);
            stmt.setString(2, jk);
            stmt.setString(3, telepon);
            stmt.setString(4, alamat);
            stmt.setString(5, prodi);
            stmt.setString(6, nim); // NIM yang akan digunakan sebagai acuan untuk data yang diubah

            // Eksekusi query
            int rowsUpdated = stmt.executeUpdate();

            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil diubah!");
            } else {
                JOptionPane.showMessageDialog(null, "NIM tidak ditemukan!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal mengubah data: " + e.getMessage());
        }
    }//GEN-LAST:event_btnUbahActionPerformed

