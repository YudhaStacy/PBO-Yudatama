    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // Ambil data dari JTextField
        String nim = txtNim.getText();
        String nama = txtNama.getText();
        String jk = ""; // bisa diambil dari radio button
        String telepon = txtTlp.getText();
        String alamat = txtAlamat.getText();
        String prodi = txtProdi.getText();

        if (rbPria.isSelected()) {
            jk = "Laki-laki";
        } else if (rbWanita.isSelected()) {
            jk = "Perempuan";
        }

        // Buat query SQL untuk menambah data
        String sql = "INSERT INTO mahasiswa (nim, nama, jk, telepon, alamat, prodi) VALUES (?, ?, ?, ?, ?, ?)";

        try {
            PreparedStatement stmt = conn.prepareStatement(sql);

            // Set parameter untuk query
            stmt.setString(1, nim);
            stmt.setString(2, nama);
            stmt.setString(3, jk);
            stmt.setString(4, telepon);
            stmt.setString(5, alamat);
            stmt.setString(6, prodi);

            // Eksekusi query
            int rowsInserted = stmt.executeUpdate();
            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan!");
            }

            tampilkanData();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal menambahkan data: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

